/** Runtime-safe metadata for snapshots created before the current category catalog. */
export function getCategoryMeta(category: unknown): CategoryMeta {
  if (typeof category === 'string' && Object.prototype.hasOwnProperty.call(categoryMeta, category)) {
    return categoryMeta[category as CategoryMeta['key']]
  }

  const fallbackLabel = typeof category === 'string' && category.trim() ? category : 'Uncategorized'
  return { key: 'career', label: fallbackLabel, tone: 'slate' }
}

