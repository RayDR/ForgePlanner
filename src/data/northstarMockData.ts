function mapSeedStatus(status: 'not-started' | 'in-progress' | 'blocked' | 'completed' | 'deferred'): MonthlyActivityStatus {
  switch (status) {
    case 'not-started':
      return 'planned'
    case 'in-progress':
      return 'in-progress'
    case 'blocked':
      return 'paused'
    case 'completed':
      return 'completed'
    case 'deferred':
      return 'skipped'
  }
}

function monthlyEntries(
  startDate: string,
  endDate: string,
  status: 'not-started' | 'in-progress' | 'blocked' | 'completed' | 'deferred',
  progress: number,
) {
  return getMonthIdsBetween(startDate, endDate).reduce<Record<string, Activity['monthlyEntries'][string]>>(
    (accumulator, monthId) => {
      accumulator[monthId] = createMonthlyEntry(monthId, mapSeedStatus(status), {
        progress,
        isSkipped: status === 'deferred',
        isPaused: status === 'blocked',
      })
      return accumulator
    },
    {},
  )
}

function createSubtasks(subtasks: string[]) {
  return subtasks.map((title, index) => ({
    id: `sub-${index + 1}-${title.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/(^-|-$)/g, '')}`,
    title,
    weight: 1,
    completed: false,
  }))
}

function createActivity(input: {
  id: string
  title: string
  description: string
  category: Activity['category']
  status: 'not-started' | 'in-progress' | 'blocked' | 'completed' | 'deferred'
  priority: Activity['priority']
  relationshipMode: RelationshipMode
  startDate: string
  endDate: string
  targetMonth: string
  milestone?: boolean
  sequenceGroupId?: string
  linkedActivityIds?: string[]
  dependencyIds?: string[]
  notes?: string
  savingsImpact?: number
  budgetImpact?: number
  estimatedHours?: number
  parentGoalId?: string
  subtasks: string[]
}) {
  const statusId =
    input.status === 'completed'
      ? 'done'
      : input.status === 'blocked'
      ? 'blocked'
      : input.status === 'deferred'
      ? 'blocked'
      : input.status === 'in-progress'
      ? 'in-progress'
      : 'planned'

  return {
    id: input.id,
    title: input.title,
    description: input.description,
    category: input.category,
    priority: input.priority,
    relationshipMode: input.relationshipMode,
    startDate: input.startDate,
    endDate: input.endDate,
    estimatedHours: input.estimatedHours,
    parentGoalId: input.parentGoalId,
    linkedActivityIds: input.linkedActivityIds ?? [],
    dependencyIds: input.dependencyIds ?? [],
    sequenceGroupId: input.sequenceGroupId,
    milestone: input.milestone ?? false,
    colorKey: colorByCategory[input.category],
    statusId,
    budgetImpact: input.budgetImpact,
    savingsImpact: input.savingsImpact,
    notes: input.notes ?? '',
    subtasks: createSubtasks(input.subtasks),
    comments: [],
    history: [
      {
        id: `hist-created-${input.id}`,
        activityId: input.id,
        type: 'created',
        message: 'Activity created from seed data',
        occurredAt: new Date().toISOString(),
      },
    ],
    monthlyEntries: monthlyEntries(input.startDate, input.endDate, input.status, input.status === 'completed' ? 100 : 0),
  } satisfies Activity
}

